import { axios, apiURL } from './config.js';

class BankAccount {
    static async getBankAccountByIdSalesman(id_salesman) {
        try {
            const response = await axios.get(`${apiURL}/bank_account/${id_salesman}`);
            return response.data;
        } catch (error) {
            if(error.response && error.response.data)
                return error.response.data
            else return {success: false, message: 'Houve um erro desconhecido'};
        }
    }
    static async create(data) {
        try {
            const response = await axios.post(`${apiURL}/bank_account`, data);
            return response.data;
        } catch (error) {
            if(error.response && error.response.data)
                return error.response.data
            else return {success: false, message: 'Houve um erro desconhecido'};
        }
    }
    static async update(data) {
        try {
            const response = await axios.put(`${apiURL}/bank_account/`, data);
            return response.data;
        } catch (error) {
            if(error.response && error.response.data)
                return error.response.data
            else return {success: false, message: 'Houve um erro desconhecido'};
        }
    }
}

export default BankAccount;